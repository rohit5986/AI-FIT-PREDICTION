import fs from 'node:fs/promises';
import path from 'node:path';

function parseArgs(argv) {
  const args = {};
  for (let i = 2; i < argv.length; i += 1) {
    const token = argv[i];
    if (!token.startsWith('--')) continue;
    const key = token.slice(2);
    const next = argv[i + 1];
    if (!next || next.startsWith('--')) {
      args[key] = true;
      continue;
    }
    args[key] = next;
    i += 1;
  }
  return args;
}

function usage() {
  console.log('Usage:');
  console.log('node scripts/buildIndianProductsOnly.mjs [--in data/scraped] [--out indianBrandsData.js]');
}

function slugify(value) {
  return String(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '')
    .slice(0, 40);
}

function inferPlatform(sourceUrl) {
  if (!sourceUrl || typeof sourceUrl !== 'string') return 'unknown';
  const lowered = sourceUrl.toLowerCase();
  if (lowered.includes('myntra.com')) return 'myntra';
  if (lowered.includes('ajio.com')) return 'ajio';
  if (lowered.includes('tatacliq.com')) return 'tatacliq';
  if (lowered.includes('bewakoof.com')) return 'bewakoof';
  return 'generic';
}

async function readJsonFiles(dirPath) {
  const absolute = path.resolve(dirPath);
  let entries = [];
  try {
    entries = await fs.readdir(absolute, { withFileTypes: true });
  } catch (error) {
    if (error && error.code === 'ENOENT') {
      return [];
    }
    throw error;
  }
  const files = entries
    .filter((entry) => entry.isFile() && entry.name.endsWith('.json'))
    .map((entry) => path.join(absolute, entry.name))
    .sort();

  const documents = [];
  for (const filePath of files) {
    const raw = await fs.readFile(filePath, 'utf8');
    const parsed = JSON.parse(raw);
    documents.push({ filePath, parsed });
  }

  return documents;
}

function buildProducts(documents) {
  const counters = new Map();
  const products = [];

  for (const doc of documents) {
    const brandId = String(doc.parsed.brandId || '').trim();
    const category = String(doc.parsed.category || '').trim();
    const platform = String(doc.parsed.platform || '').trim();
    const scrapedProducts = Array.isArray(doc.parsed.products) ? doc.parsed.products : [];

    if (!brandId || !['top', 'bottom'].includes(category)) {
      continue;
    }

    const keyBase = `${brandId}:${category}`;
    if (!counters.has(keyBase)) counters.set(keyBase, 1);

    for (const entry of scrapedProducts) {
      const name = String(entry?.name || '').trim();
      const priceNum = Number(entry?.price);
      const sourceUrl = String(entry?.source || '').trim();
      const imageUrl = String(entry?.imageUrl || '').trim();
      const sourcePlatform = platform || inferPlatform(sourceUrl);
      if (!name || !Number.isFinite(priceNum) || priceNum <= 0) continue;

      const sequence = counters.get(keyBase);
      counters.set(keyBase, sequence + 1);

      const baseSlug = slugify(name) || `${brandId}-${category}-${sequence}`;
      const id = `${brandId}-${category}-${String(sequence).padStart(3, '0')}`;
      const roundedPrice = Math.round(priceNum);
      const roundedOriginal = Math.max(roundedPrice, Math.round(roundedPrice * 1.25));

      products.push({
        id,
        brandId,
        platform: sourcePlatform,
        name,
        category,
        price: roundedPrice,
        originalPrice: roundedOriginal,
        quality: 4.1,
        rating: 4.2,
        reviews: 80 + sequence * 7,
        image: category === 'top' ? '👕' : '👖',
        imageUrl: imageUrl || undefined,
        colors: ['Black', 'White', 'Navy'],
        sizes: ['S', 'M', 'L', 'XL'],
        source: sourceUrl || undefined,
        description: `Auto-imported listing (${baseSlug}) from scraped source.`,
        details: {
          material: 'To be updated',
          fit: 'Regular',
          care: 'Machine wash',
          inStock: 60
        }
      });
    }
  }

  const dedupe = new Map();
  for (const item of products) {
    const key = `${item.brandId}::${item.platform}::${item.name.toLowerCase()}::${item.category}`;
    if (!dedupe.has(key)) dedupe.set(key, item);
  }

  return [...dedupe.values()];
}

async function writeModule(outputPath, products, inputDir) {
  const absoluteOutput = path.resolve(outputPath);
  const text = [
    '// Auto-generated by scripts/buildIndianProductsOnly.mjs',
    `// Source directory: ${inputDir}`,
    `// Generated at: ${new Date().toISOString()}`,
    '',
    'export const INDIAN_BRANDS = [];',
    '',
    `export const INDIAN_PRODUCTS = ${JSON.stringify(products, null, 2)};`,
    ''
  ].join('\n');

  await fs.writeFile(absoluteOutput, text, 'utf8');
}

async function main() {
  const args = parseArgs(process.argv);
  if (args.help) {
    usage();
    process.exit(0);
  }

  const inputDir = args.in || 'data/scraped';
  const outputPath = args.out || 'indianBrandsData.js';

  let documents;
  try {
    documents = await readJsonFiles(inputDir);
  } catch (error) {
    console.error(`Failed to read scraped data at ${inputDir}: ${error.message}`);
    process.exit(1);
  }

  const products = buildProducts(documents);
  await writeModule(outputPath, products, inputDir);

  console.log(`Processed ${documents.length} scraped files.`);
  console.log(`Generated ${products.length} product records in ${path.resolve(outputPath)}.`);
}

main().catch((error) => {
  console.error('Build failed:', error.message);
  process.exit(1);
});
