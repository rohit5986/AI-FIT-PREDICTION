import fs from 'node:fs/promises';
import path from 'node:path';

function usage() {
  console.log('Usage:');
  console.log('node scripts/buildIndianDataModule.mjs <input-json> [output-js]');
  console.log('Example:');
  console.log('node scripts/buildIndianDataModule.mjs data/manual/indianBrands.template.json indianBrandsData.js');
}

function ensureArray(value, label) {
  if (!Array.isArray(value)) {
    throw new Error(`${label} must be an array`);
  }
}

function validateBrand(brand) {
  if (!brand || typeof brand !== 'object') throw new Error('Each brand must be an object');
  if (!brand.id || typeof brand.id !== 'string') throw new Error('Brand id is required');
  if (!brand.name || typeof brand.name !== 'string') throw new Error('Brand name is required');
  if (!brand.chart || typeof brand.chart !== 'object') throw new Error(`Brand ${brand.id} chart is required`);
}

function validateProduct(product) {
  if (!product || typeof product !== 'object') throw new Error('Each product must be an object');
  if (!product.id || typeof product.id !== 'string') throw new Error('Product id is required');
  if (!product.brandId || typeof product.brandId !== 'string') throw new Error(`Product ${product.id} brandId is required`);
  if (!product.name || typeof product.name !== 'string') throw new Error(`Product ${product.id} name is required`);
  if (!['top', 'bottom'].includes(product.category)) {
    throw new Error(`Product ${product.id} category must be top or bottom`);
  }
  if (!Number.isFinite(Number(product.price))) throw new Error(`Product ${product.id} price must be a number`);
}

async function main() {
  const inputPath = process.argv[2];
  const outputPath = process.argv[3] || 'indianBrandsData.js';

  if (!inputPath) {
    usage();
    process.exit(1);
  }

  const absoluteInput = path.resolve(inputPath);
  const absoluteOutput = path.resolve(outputPath);

  const raw = await fs.readFile(absoluteInput, 'utf8');
  const parsed = JSON.parse(raw);

  const brands = parsed.brands || [];
  const products = parsed.products || [];

  ensureArray(brands, 'brands');
  ensureArray(products, 'products');

  brands.forEach(validateBrand);
  products.forEach(validateProduct);

  const fileText = [
    '// Auto-generated by scripts/buildIndianDataModule.mjs',
    `// Source: ${inputPath}`,
    '',
    `export const INDIAN_BRANDS = ${JSON.stringify(brands, null, 2)};`,
    '',
    `export const INDIAN_PRODUCTS = ${JSON.stringify(products, null, 2)};`,
    ''
  ].join('\n');

  await fs.writeFile(absoluteOutput, fileText, 'utf8');
  console.log(`Wrote ${brands.length} brands and ${products.length} products to ${absoluteOutput}`);
}

main().catch((error) => {
  console.error('Build failed:', error.message);
  process.exit(1);
});
